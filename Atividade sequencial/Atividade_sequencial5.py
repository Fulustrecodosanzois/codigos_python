""" 5. Um barril de refresco é feito com 8 partes (80%) de água mineral e 2 partes (20%) de suco de maracujá. Faça um algoritmo para calcular quantos litros de água e de suco são necessários para se fazer X litros de refresco. A quantidade de litros de refresco deve ser informada pelo usuário """

